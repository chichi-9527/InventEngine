	void IEventLayer::AddEventObj(IBaseEventFunction* obj)
	{
		if (obj)
			Objs.push_back(obj);
	}

	void IEventLayer::EraseEventObj(IBaseEventFunction * obj)
	{
		if (obj)
		{
			auto iter = std::find(Objs.begin(), Objs.end(), obj);
			if (iter != Objs.end())
			{
				Objs.erase(iter);
			}
		}
	}

